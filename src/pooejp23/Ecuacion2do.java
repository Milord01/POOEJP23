    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        int a,b,c;
	double discriminante,s1,s2;
        
        a=Integer.parseInt(txtA.getText());
        b=Integer.parseInt(txtB.getText());
        c=Integer.parseInt(txtC.getText());
        
        discriminante = (Math.pow(b, 2) - (4 * a * c));
        
        if(discriminante>0) {
	 	s1=((-b) + Math.sqrt(Math.pow(b, 2) - (4 * a * c))) / (2 * a);
	 	s2=((-b) - Math.sqrt(Math.pow(b, 2) - (4 * a * c))) / (2 * a);
                txtSolucion1.setText(String.valueOf(s1));
                txtSolucion2.setText(String.valueOf(s2));
	 }
	 else if(discriminante==0) {
	 	txtSolucion1.setText(String.valueOf(((-b) - Math.sqrt(Math.pow(b, 2) - (4 * a * c))) / (2 * a)));
                txtSolucion2.setText("No hay solucion 2 ");
	 }
	 else {
	 	txtSolucion1.setText("Sin soluciones posibles");
                txtSolucion2.setText("Sin soluciones posibles");
	 }


    }//GEN-LAST:event_btnCalcularActionPerformed

